const isPreviewBot = async (): Promise<boolean> => {
  const h = await headers(); // artÄ±k asenkron
  const userAgent = h.get('User-Agent')?.toLowerCase() ?? '';

  const knownBots = ['bot', 'crawler', 'spider', 'facebook', 'twitter', 'linkedin', 'preview'];

  return knownBots.some((b) => userAgent.includes(b));
};

export default isPreviewBot;
